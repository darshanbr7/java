class whileloop{
	public static void main(String args[]){
		// while loop for incrment order
		int i=0;
		System.out.println("incrment order");
		while (i<=10){
			System.out.println(i);
			i++;
		}
		// while loop for decrment order
		int j=10;
		System.out.println("Decrment order");
		while(j>=0){
			System.out.println(j);
			j--;
		}
	}
}