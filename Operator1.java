class incr{
	public static void main(String args[]){
		// increment and decrement operator
		// post increment
		int a=10;
		System.out.println(a++);
		System.out.println(a);
		// in post increment -->it first print print the value and then increses the value
		
		// pre increment
		System.out.println(++a);
		// in pre increment --> it first increment the value and then the print the value
		
		// decrement
		// post decrement
		int b=15;
		System.out.println(b--);
		System.out.println(b);
		// in post decrement -->it first print  the value and then decrement the value
		
		System.out.println(--b);
		// in pre decrement -->it first decrement  the value and then print the value
	}
}